function makeIconSVG(iconName, color, width, height) {
  const paths = ICON_PATHS[iconName] || ICON_PATHS['Star'];
  return `<svg xmlns="http://www.w3.org/2000/svg" width="${width}" height="${height}" viewBox="0 0 24 24" fill="none" stroke="${color}" stroke-width="1.8" stroke-linecap="round" stroke-linejoin="round">${paths}</svg>`;
}

function useIconImage(iconName, color, width, height) {
  const [img, setImg] = useState(null);

  useEffect(() => {
    let cancelled = false;
    const svgStr = makeIconSVG(iconName, color, width, height);
    const blob = new Blob([svgStr], { type: 'image/svg+xml;charset=utf-8' });
    const url = URL.createObjectURL(blob);
    const image = new window.Image();
    image.onload = () => {
      if (!cancelled) setImg(image);
      URL.revokeObjectURL(url);
    };
    image.onerror = () => URL.revokeObjectURL(url);
    image.src = url;
    return () => { cancelled = true; };
  }, [iconName, color, width, height]);

  return img;
}

export default function IconLayer({ layer, scale, onSelect, onDragEnd, onTransformEnd }) {
  const w = (layer.width || 80) * scale;
  const h = (layer.height || 80) * scale;
  const img = useIconImage(layer.iconName, layer.fill || '#ffffff', Math.round(w * 2), Math.round(h * 2));

  return (
    <KonvaImage
      id={layer.id}
      image={img}
      x={layer.x * scale}
      y={layer.y * scale}
      width={w}
      height={h}
      rotation={layer.rotation || 0}
      opacity={layer.opacity ?? 1}
      draggable={!layer.locked}
      onClick={onSelect}
      onTap={onSelect}
      onDragEnd={onDragEnd}
      onTransformEnd={onTransformEnd}
    />
  );
}
